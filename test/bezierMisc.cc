void testMatrixInverse(){


  mth::Matrix<double> A(35,35);
  for (int i = 0; i < 35; ++i)
    for (int j = 0; j < 35; ++j)
      A(i,j) = a_data[i][j];
  mth::Matrix<double> AiQR(35,35), AiPLU(35,35);

  crv::invertMatrixWithPLU(35,A,AiPLU);
  crv::invertMatrixWithQR(35,A,AiQR);

  mth::Matrix<double> eyeQR(35,35), eyePLU(35,35);
  mth::multiply(A,AiQR,eyeQR);
  mth::multiply(A,AiPLU,eyePLU);

  for (int i = 0; i < 35; ++i)
    for (int j = 0; j < 35; ++j){
      PCU_ALWAYS_ASSERT(fabs(AiQR(i,j) - a_invdata[i][j]) < 1e-14);
      PCU_ALWAYS_ASSERT(fabs(eyeQR(i,j) - (i == j)) < 1e-14);
      PCU_ALWAYS_ASSERT(fabs(AiPLU(i,j) - a_invdata[i][j]) < 1e-14);
      PCU_ALWAYS_ASSERT(fabs(eyePLU(i,j) - (i == j)) < 1e-14);
    }

}

